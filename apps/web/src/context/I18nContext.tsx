export type TranslationKey = keyof (typeof DICT)['es'];

interface I18nCtx {
  locale: Locale;
  setLocale: (l: Locale) => void;
  t: (key: TranslationKey) => string;
}

const Ctx = createContext<I18nCtx | null>(null);

const VALID_LOCALES: readonly Locale[] = ['es', 'en', 'pt', 'fr', 'de', 'it', 'ja', 'zh'];

export function I18nProvider({ children }: { children: ReactNode }) {
  const [locale, setLocaleState] = useState<Locale>(() => {
    if (typeof window === 'undefined') return 'es';
    const saved = localStorage.getItem('gdp_locale') as Locale | null;
    if (saved && VALID_LOCALES.includes(saved)) return saved;
    // Sin preferencia guardada: detecta el idioma del navegador entre los soportados.
    const browserLang = navigator.language.slice(0, 2) as Locale;
    return VALID_LOCALES.includes(browserLang) ? browserLang : 'es';
  });

  const setLocale = useCallback((l: Locale) => {
    setLocaleState(l);
    localStorage.setItem('gdp_locale', l);
  }, []);

  const t = useCallback((key: TranslationKey): string => DICT[locale][key], [locale]);

  return <Ctx.Provider value={{ locale, setLocale, t }}>{children}</Ctx.Provider>;
}

export function useI18n() {
  const ctx = useContext(Ctx);
  if (!ctx) throw new Error('useI18n must be inside I18nProvider');
  return ctx;
}
